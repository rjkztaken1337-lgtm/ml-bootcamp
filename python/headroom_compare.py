# ============================================================
#  СРАВНЕНИЕ: что Headroom сжимает лучше?
#  Три типа контента: JSON, обычный текст, код
# ============================================================

import json
import headroom


# --- Вспомогательная функция: сжать один текст и вернуть цифры ---
def compress_text(text, model_limit=1500):
    # ВАЖНО: роль "tool" — это результат вызова инструмента (как будто
    # агент дёрнул API/прочитал файл). Headroom считает такой контент
    # "безопасным для сжатия" и не защищает его.
    # Роль "assistant" по умолчанию ЗАЩИЩЕНА от сжатия (чтобы не ломать
    # кэш на стороне провайдера) — именно поэтому в первом прогоне
    # текст и код не сжимались, а JSON (тоже был "assistant") сжался
    # только потому, что структурный JSON-компрессор (SmartCrusher) не
    # подчиняется этому правилу — он для JSON-массивов работает всегда.
    messages = [
        {"role": "user", "content": "Проанализируй это:"},
        {"role": "tool", "content": text},
    ]
    # Тот же конфиг, что работал в прошлом демо
    config = headroom.CompressConfig(
        protect_recent=0,
        min_tokens_to_compress=100,
        compress_user_messages=True,
    )
    return headroom.compress(messages, model_limit=model_limit, config=config)


# --- Красивый вывод одной строки таблицы ---
def report(label, result):
    before = result.tokens_before
    after = result.tokens_after
    # Процент экономии; защищаемся от деления на ноль
    percent = 100 * result.tokens_saved / before if before else 0
    # :<8 — выравнивание по левому краю, :>6 — по правому
    print(f"{label:<8} | {before:>6} | {after:>6} | {percent:>6.1f}%")


# ============================================================
#  1. JSON — структурированные данные (как ответ API)
# ============================================================
data = {
    "products": [
        {
            "id": i,
            "title": f"Product {i}",
            "price": 100 + i,
            "in_stock": True,
            "tags": ["new", "sale", "popular"],
        }
        for i in range(40)
    ]
}
json_text = json.dumps(data, indent=2, ensure_ascii=False)

# ============================================================
#  2. ОБЫЧНЫЙ ТЕКСТ — проза (как статья или документация)
# ============================================================
paragraph = (
    "Машинное обучение — это раздел искусственного интеллекта, "
    "который изучает методы построения алгоритмов, способных обучаться "
    "на данных. Вместо жёстко заданных правил модель находит "
    "закономерности сама. Чем больше качественных данных, тем лучше "
    "результат. Это основа современных систем рекомендаций, "
    "распознавания речи и компьютерного зрения. "
)
plain_text = paragraph * 8  # повторяем, чтобы объём был сравним

# ============================================================
#  3. КОД — исходник на Python
# ============================================================
code_snippet = '''
def calculate_discount(price, percent):
    """Считает цену со скидкой."""
    discount = price * percent / 100
    final = price - discount
    return final

def apply_tax(price, tax_rate=20):
    """Добавляет налог к цене."""
    tax = price * tax_rate / 100
    return price + tax
'''
code_text = code_snippet * 6  # повторяем для объёма


# --- Важно: compress() сжимает только когда есть "давление контекста" —
# то есть текущий объём ПРЕВЫШАЕТ model_limit. Раньше мы брали
# model_limit=1500, а текст/код (799/509 токенов) были МЕНЬШЕ лимита —
# поэтому сжимать было "незачем", и Headroom справедливо пропускал их
# без изменений. Возьмём одинаковый маленький лимит (200) для всех трёх,
# чтобы каждый пример гарантированно оказался "тесно".
LIMIT = 200

print("Прогреваем ML-модель Kompress (первая загрузка в память)...")
import time

compress_text(plain_text, model_limit=LIMIT)
time.sleep(20)  # даём фоновому потоку время загрузить веса модели
print("Прогрев завершён.\n")

# --- Прогоняем все три и печатаем таблицу ---
print("=" * 45)
print(f"{'ТИП':<8} | {'ДО':>6} | {'ПОСЛЕ':>6} | {'ЭКОНОМИЯ':>7}")
print("=" * 45)
report("JSON", compress_text(json_text, model_limit=LIMIT))
report("ТЕКСТ", compress_text(plain_text, model_limit=LIMIT))
report("КОД", compress_text(code_text, model_limit=LIMIT))
print("=" * 45)
