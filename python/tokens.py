# tiktoken — библиотека, которая разбивает текст на токены
# так же, как это делают модели. Импортируем её.
import tiktoken

# Берём "кодировщик" (encoder). cl100k_base — популярная схема
# токенизации. Это объект, который умеет text -> токены и обратно.
enc = tiktoken.get_encoding("cl100k_base")


# --- Функция: посчитать и показать токены для одной строки ---
def show_tokens(text):
    # .encode(text) превращает строку в список чисел (id токенов)
    token_ids = enc.encode(text)

    # len() — длина списка, то есть СКОЛЬКО токенов получилось
    count = len(token_ids)

    # А теперь раскодируем каждый токен обратно в текст,
    # чтобы увидеть, на какие кусочки разбилась строка.
    pieces = [enc.decode([tid]) for tid in token_ids]

    # f-string — подставляем переменные прямо в строку
    print(f"Текст:    {text!r}")
    print(f"Токенов:  {count}")
    print(f"Кусочки:  {pieces}")
    print("-" * 50)

    # return — возвращаем число токенов, пригодится дальше
    return count


# --- Пробуем на разных примерах ---
show_tokens("кот")
show_tokens("программирование")
show_tokens("Hello, world!")
show_tokens("Привет, мир!")


# --- Сравним "стоимость" русского и английского ---
ru = "Машинное обучение — это интересно"
en = "Machine learning is interesting"

ru_count = show_tokens(ru)
en_count = show_tokens(en)

# Простое сравнение через if/else (повторяем условия!)
print(f"Русский:    {ru_count} токенов")
print(f"Английский: {en_count} токенов")
if ru_count > en_count:
    print("=> Русский текст 'дороже' в токенах!")
else:
    print("=> Английский вышел дороже или столько же.")
